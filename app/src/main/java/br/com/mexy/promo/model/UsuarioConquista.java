package br.com.mexy.promo.model;

public class UsuarioConquista {

	private Integer usuario_id;
	private Integer conquista_id;

	public Integer getUsuario() {
		return usuario_id;
	}

	public void setUsuario(Integer usuario_id) {
		this.usuario_id = usuario_id;
	}

	public Integer getConquista() {
		return conquista_id;
	}

	public void setConquista(Integer conquista_id) {
		this.conquista_id = conquista_id;
	}

}
